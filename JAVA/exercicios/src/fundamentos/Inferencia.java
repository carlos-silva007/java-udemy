package fundamentos;

public class Inferencia {

	public static void main(String[] args) {
		
		long y = 956656563;
		System.out.println(y);
		
		int a = 568860062;
		System.out.println(a);
		
		var b = 5.0;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
		c = "Outro Texto";
		System.out.println(c);
		
		System.out.println(a + b + c);
	}
}
