//duas expressoes resultar em TRUE (op. Aritmeticos/Relacionais/Logicos)
let verdadeira1 = 2 + 5 >= 7 && 8 > 2;
let verdadeira2 = !(6 * 6 < 3);

console.log(verdadeira1);
console.log(verdadeira2);

//Duas expressoes resultar em FALSE (Aritmeticos/Relacionais/Logicos)
let false1 = 8 - 4 > 12 || false || 15 === "15";
let false2 = !(7 !== 7 || "3" !== 3);

console.log(false1);
console.log(false2);


